package cmu.edu.petrinet;

public class Param {
	private String paramTyp;
	private int paramCnt;

	public String getParamTyp() {
		return paramTyp;
	}

	public void setParamTyp(String typ) {
		paramTyp = typ;
	}

	public int getParamCnt() {
		return paramCnt;
	}

	public void setParamCnt(int cnt) {
		paramCnt = cnt;
	}
}